# INTERACTIVE CLI
# ============================================================
def interactive_mode():
    """Run the interactive article selection and PDF generation."""

    print("\n" + "=" * 60)
    print("  GEISTER MEDIZINTECHNIK")
    print("  Custom Product Catalog Generator")
    print("=" * 60)

    # Scan catalogs first
    print("\n  Scanning catalogs...")
    index, _ = build_article_index(CATALOG_FILES)
    all_articles = sorted(index.keys())
    print(f"  Found {len(all_articles)} articles in {len(CATALOG_FILES)} catalog(s).\n")

    # Customer info
    print("--- Customer Information ---")
    customer_name = input("  Customer name: ").strip()
    if not customer_name:
        customer_name = "Customer"

    customer_company = input("  Company: ").strip()
    if not customer_company:
        customer_company = ""

    # Article selection
    selected = []
    print("\n--- Article Selection ---")
    print("  Enter article numbers (e.g., 34-7286, 29-1000)")
    print("  Commands:")
    print("    'list'   - Show all available articles")
    print("    'search' - Search by keyword or partial number")
    print("    'done'   - Finish selection and generate PDF")
    print("    'clear'  - Clear current selection")
    print("    'show'   - Show current selection")
    print()

    while True:
        user_input = input("  Article / Command > ").strip()

        if not user_input:
            continue

        cmd = user_input.lower()

        if cmd == "done":
            if not selected:
                print("  [!] No articles selected. Add at least one.")
                continue
            break

        elif cmd == "list":
            print(f"\n  All available articles ({len(all_articles)}):")
            for art in all_articles:
                locs = index[art]
                catalog = os.path.basename(locs[0][0]).split("_")[0]
                pages = ", ".join([str(l[1] + 1) for l in locs])
                print(f"    {art:<18} ({catalog}, page {pages})")
            print()
            continue

        elif cmd == "clear":
            selected.clear()
            print("  [OK] Selection cleared.")
            continue

        elif cmd == "show":
            if not selected:
                print("  [i] No articles selected yet.")
            else:
                print(f"\n  Current selection ({len(selected)} articles):")
                for art in selected:
                    print(f"    {art}")
            print()
            continue

        elif cmd.startswith("search"):
            parts = cmd.split(maxsplit=1)
            query = parts[1] if len(parts) > 1 else input("  Search term: ").strip()
            query_upper = query.upper()
            results = [a for a in all_articles if query_upper in a.upper()]
            if results:
                print(f"\n  Found {len(results)} match(es):")
                for art in results:
                    print(f"    {art}")
                print()
            else:
                print(f"  [!] No results for '{query}'")
            continue

        # Try to match the input as an article number
        art_input = user_input.strip()

        # Exact match?
        if art_input in index:
            if art_input in selected:
                print(f"  [i] {art_input} already selected.")
            else:
                selected.append(art_input)
                print(f"  [+] Added: {art_input}")
            continue

        # Partial/fuzzy match
        matches = [a for a in all_articles if art_input.upper() in a.upper()]
        if not matches:
            print(f"  [!] '{art_input}' not found. Try 'list' or 'search'.")
            continue

        if len(matches) == 1:
            art = matches[0]
            if art in selected:
                print(f"  [i] {art} already selected.")
            else:
                selected.append(art)
                print(f"  [+] Added: {art}")
        else:
            print(f"\n  Multiple matches for '{art_input}':")
            for i, art in enumerate(matches, 1):
                sel_mark = " *" if art in selected else ""
                print(f"    {i}. {art}{sel_mark}")

            choice = input("  Select number(s), 'all', or Enter to skip: ").strip()
            if choice.lower() == "all":
                for art in matches:
                    if art not in selected:
                        selected.append(art)
                        print(f"  [+] Added: {art}")
            elif choice:
                try:
                    indices = [int(x.strip()) - 1 for x in choice.split(",")]
                    for idx in indices:
                        if 0 <= idx < len(matches):
                            art = matches[idx]
                            if art not in selected:
                                selected.append(art)
                                print(f"  [+] Added: {art}")
                except ValueError:
                    print("  [!] Invalid input.")

    # Generate
    print("\n--- Generating PDF ---")
    output = generate_catalog(customer_name, customer_company, selected)

    if output:
        print(f"\n  Done! Open the file: {output}")


