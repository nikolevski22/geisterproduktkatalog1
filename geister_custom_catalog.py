# MAIN PDF GENERATOR
# ============================================================
def generate_catalog(customer_name, customer_company, article_numbers,
                     output_path=None, catalog_files=None, cached_index=None):
    """
    Generate a customized catalog PDF.

    Process:
    1. Use cached article index (or build from source catalogs)
    2. Find which pages contain the requested articles
    3. Copy those original pages (with all images!) into a new PDF
    4. Add highlight overlays on the requested article numbers
    5. Add cover + closing pages

    Args:
        customer_name: Customer's name
        customer_company: Customer's company
        article_numbers: List of article number strings
        output_path: Optional output file path
        catalog_files: Optional list of catalog PDF paths
        cached_index: Optional pre-built article index dict {art_no: [(pdf_path, page_idx), ...]}

    Returns:
        Path to the generated PDF, or None on failure
    """
    if catalog_files is None:
        catalog_files = CATALOG_FILES

    if cached_index is not None:
        print("\n  Using cached article index...")
        index = cached_index
        page_dims = {}
    else:
        print("\n  Scanning catalogs...")
        index, page_dims = build_article_index(catalog_files)
    print(f"  Found {len(index)} unique articles in index.")

    # Resolve requested articles
    found_articles = OrderedDict()
    not_found = []

    for art_no in article_numbers:
        art_no = art_no.strip()
        if art_no in index:
            found_articles[art_no] = index[art_no]
        else:
            # Try case-insensitive or partial match
            matched = False
            for idx_art in index:
                if idx_art.upper() == art_no.upper():
                    found_articles[idx_art] = index[idx_art]
                    matched = True
                    break
            if not matched:
                not_found.append(art_no)

    if not_found:
        print(f"  [!] Not found in catalogs: {', '.join(not_found)}")

    if not found_articles:
        print("  [!] No valid articles found. Aborting.")
        return None

    # Determine which pages to include (deduplicate)
    # Each page should only appear once, even if multiple articles are on it
    # Track: (pdf_path, page_idx) -> set of article numbers to highlight
    pages_to_include = OrderedDict()

    for art_no, locations in found_articles.items():
        # Pick the best page for this article (prefer pages with tables/data)
        # Usually the page with the most article numbers = the data page
        best_location = locations[0]

        # If article appears on multiple pages, prefer pages > 1
        # (page 0 is usually the cover)
        for loc in locations:
            if loc[1] > 0:  # Prefer non-cover pages
                best_location = loc
                break

        key = (best_location[0], best_location[1])
        if key not in pages_to_include:
            pages_to_include[key] = set()
        pages_to_include[key].add(art_no)

    print(f"  Selected {len(found_articles)} articles on {len(pages_to_include)} catalog page(s).")

    # Get page dimensions from first page to include (or default)
    pw, ph = 680.315, 481.89  # Default A4 landscape in points
    if pages_to_include:
        first_key = list(pages_to_include.keys())[0]
        try:
            reader_tmp = PdfReader(first_key[0])
            p = reader_tmp.pages[first_key[1]]
            pw = float(p.mediabox.width)
            ph = float(p.mediabox.height)
        except Exception:
            pass
    elif page_dims:
        first_pdf = catalog_files[0] if catalog_files else None
        if first_pdf and first_pdf in page_dims:
            pw, ph = page_dims[first_pdf]

    # Build the output PDF
    writer = PdfWriter()

    # 1. Add cover page
    cover = create_cover_page(pw, ph, customer_name, customer_company,
                              len(found_articles), len(pages_to_include))
    writer.add_page(cover)

    # 2. Add original catalog pages with highlights + new page numbering
    current_page = 2  # Cover is page 1
    for (pdf_path, page_idx), highlight_articles in pages_to_include.items():
        reader = PdfReader(pdf_path)
        orig_page = reader.pages[page_idx]

        # Find positions of all articles to highlight on this page
        all_positions = []
        for art_no in highlight_articles:
            positions = find_article_positions(pdf_path, page_idx, art_no)
            all_positions.extend(positions)

        # Create overlay with highlights + new page number + source reference
        page_w = float(orig_page.mediabox.width)
        page_h = float(orig_page.mediabox.height)
        catalog_filename = os.path.basename(pdf_path)
        original_page = page_idx + 1  # 1-based

        overlay = create_page_overlay(
            page_w, page_h, all_positions,
            new_page_num=current_page,
            source_catalog=catalog_filename,
            original_page_num=original_page
        )
        orig_page.merge_page(overlay)

        writer.add_page(orig_page)

        arts_str = ", ".join(sorted(highlight_articles))
        print(f"  + p.{current_page}: {catalog_filename} (orig. p.{original_page}) -> {arts_str}")
        current_page += 1

    # 3. Add closing page
    back = create_back_page(pw, ph)
    writer.add_page(back)

    # Write output
    if not output_path:
        safe_name = re.sub(r'[^\w\s-]', '', customer_company or customer_name)
        safe_name = re.sub(r'\s+', '_', safe_name).strip('_')
        date_str = datetime.now().strftime("%Y%m%d")
        filename = f"Geister_Selection_{safe_name}_{date_str}.pdf"
        output_path = os.path.join(SCRIPT_DIR, filename)

    with open(output_path, "wb") as f:
        writer.write(f)

    print(f"\n{'='*60}")
    print(f"  PDF generated successfully!")
    print(f"  Output: {output_path}")
    print(f"  Articles: {len(found_articles)}")
    print(f"  Pages: {len(pages_to_include) + 2} (incl. cover + closing)")
    print(f"  Customer: {customer_name}, {customer_company}")
    print(f"{'='*60}")

    return output_path


