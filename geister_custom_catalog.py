def create_cover_page(page_width, page_height, customer_name, customer_company,
                      article_count, page_count):
    """Create a branded Geister cover page."""
    buf = BytesIO()
    c = rl_canvas.Canvas(buf, pagesize=(page_width, page_height))

    w, h = page_width, page_height

    # ---- Background elements ----

    # White base
    c.setFillColor(white)
    c.rect(0, 0, w, h, fill=1, stroke=0)

    # Light gray lower section
    c.setFillColor(HexColor("#F2F2F2"))
    c.rect(0, 0, w, h * 0.35, fill=1, stroke=0)

    # Red circle accent (top-right, partially clipped)
    c.setFillColor(GEISTER_RED)
    c.circle(w + 10, h - 60, 140, fill=1, stroke=0)

    # Gray circle accent (right-center)
    c.setFillColor(HexColor("#C8C8C8"))
    c.circle(w - 30, h * 0.30, 110, fill=1, stroke=0)

    # ---- Geister Logo (top-left) ----

    logo_x = 30
    logo_y = h - 75
    logo_w = 55
    logo_h = 55

    # Red square with rounded corners
    c.setFillColor(GEISTER_RED)
    c.roundRect(logo_x, logo_y, logo_w, logo_h, 3, fill=1, stroke=0)

    # White "G" inside the square - large serif italic
    c.setFillColor(white)
    c.setFont("Times-BoldItalic", 42)
    c.drawString(logo_x + 10, logo_y + 10, "G")

    # "GEISTER" text below logo
    c.setFillColor(GEISTER_RED)
    c.setFont("Helvetica", 7.5)
    c.drawString(logo_x, logo_y - 12, "G E I S T E R")

    # ---- Red accent line (like in original catalog headers) ----
    c.setFillColor(GEISTER_RED)
    c.rect(0, h * 0.60, w * 0.55, 2, fill=1, stroke=0)

    # ---- Main Title ----
    title_y = h * 0.52
    c.setFillColor(HexColor("#1A1A1A"))
    c.setFont("Helvetica-Bold", 38)
    c.drawString(30, title_y, "CUSTOM PRODUCT")
    c.drawString(30, title_y - 44, "SELECTION")

    # "HIGHLIGHT" subtitle
    c.setFillColor(GEISTER_GRAY)
    c.setFont("Helvetica", 11)
    c.drawString(30, title_y - 72, "H I G H L I G H T")

    # ---- Customer Info Box (below title, no overlap) ----
    box_x = 30
    box_y = h * 0.12
    box_w = 350
    box_h = 80

    # White box with red border
    c.setFillColor(white)
    c.setStrokeColor(GEISTER_RED)
    c.setLineWidth(1.2)
    c.roundRect(box_x, box_y, box_w, box_h, 4, fill=1, stroke=1)

    # "Prepared for:" label
    c.setFillColor(GEISTER_RED)
    c.setFont("Helvetica", 8)
    c.drawString(box_x + 12, box_y + box_h - 16, "Prepared for:")

    # Customer name (bold, larger)
    c.setFillColor(HexColor("#1A1A1A"))
    c.setFont("Helvetica-Bold", 18)
    c.drawString(box_x + 12, box_y + box_h - 38, customer_name)

    # Company name
    c.setFillColor(GEISTER_GRAY)
    c.setFont("Helvetica", 11)
    c.drawString(box_x + 12, box_y + 10, customer_company)

    # Date (right-aligned in box)
    date_str = datetime.now().strftime("%d.%m.%Y")
    c.drawRightString(box_x + box_w - 12, box_y + 10, date_str)

    # ---- Document info (article count) ----
    c.setFillColor(GEISTER_GRAY)
    c.setFont("Helvetica", 8)
    info_text = f"{article_count} article(s) | {page_count} catalog page(s)"
    c.drawString(box_x + 12, box_y - 14, info_text)

    # ---- Footer ----
    c.setFillColor(GEISTER_GRAY)
    c.setFont("Helvetica", 6.5)
    c.drawString(
        30, 18,
        "GEISTER MEDIZINTECHNIK GMBH  |  Föhrenstrasse 2  |  "
        "D-78532 Tuttlingen  |  Germany  |  www.geister.com"
    )

    # Small tagline
    c.setFont("Helvetica-Oblique", 7)
    c.drawRightString(w - 30, 18, "The better way to operate\u2122")

    c.save()
    buf.seek(0)
    return PdfReader(buf).pages[0]


