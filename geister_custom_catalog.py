#!/usr/bin/env python3
"""
Geister Medizintechnik - Customized PDF Catalog Generator
==========================================================
Generates a customer-specific product catalog PDF by extracting
ORIGINAL catalog pages (with all images) and highlighting the
requested article numbers.

Usage:
    python3 geister_custom_catalog.py

The script will interactively ask for:
  - Customer name and company
  - Article numbers (from either catalog)

It then copies the relevant original catalog pages into a new PDF,
highlights the requested articles, and adds a branded cover page.

Requirements:
    pip install pypdf pdfplumber reportlab
"""

import os
import sys
import re
from datetime import datetime
from collections import OrderedDict

import pdfplumber
from pypdf import PdfReader, PdfWriter
from reportlab.lib.pagesizes import landscape
from reportlab.lib.units import mm
from reportlab.lib.colors import HexColor, white, Color
from reportlab.pdfgen import canvas as rl_canvas
from io import BytesIO

# ============================================================
# GEISTER CI/CD DESIGN CONSTANTS
# ============================================================
GEISTER_RED = HexColor("#B5121B")
GEISTER_DARK_RED = HexColor("#8C0E15")
GEISTER_GRAY = HexColor("#58585A")
GEISTER_LIGHT_GRAY = HexColor("#E8E8E8")
HIGHLIGHT_COLOR = Color(0.71, 0.07, 0.11, alpha=0.15)  # Semi-transparent red
HIGHLIGHT_BORDER = HexColor("#B5121B")

# ============================================================
# SCRIPT DIRECTORY
# ============================================================
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))

def discover_catalog_files(directory=None):
    """
    Automatically discover all PDF catalog files in the script directory.
    Excludes any files generated by this script (Geister_Selection_*.pdf).
    """
    scan_dir = directory or SCRIPT_DIR
    catalogs = []
    for f in sorted(os.listdir(scan_dir)):
        if f.lower().endswith(".pdf") and not f.startswith("Geister_Selection_"):
            catalogs.append(os.path.join(scan_dir, f))
    return catalogs


CATALOG_FILES = discover_catalog_files()

# Article number regex
ART_PATTERN = re.compile(r'\b(\d{2}-\d{4}(?:\.\w+)?(?:/\w+)?)\b')


# ============================================================
# INDEX BUILDER - Scans PDFs to map articles to pages
# ============================================================
def build_article_index(catalog_files):
    """
    Scan all catalog PDFs and build an index:
    article_number -> [(pdf_path, page_index, catalog_name)]

    Also returns page dimensions for each PDF.
    """
    index = {}
    page_dims = {}

    for pdf_path in catalog_files:
        if not os.path.exists(pdf_path):
            print(f"  [!] Catalog not found: {pdf_path}")
            continue

        catalog_name = os.path.basename(pdf_path).replace(".pdf", "")

        with pdfplumber.open(pdf_path) as pdf:
            page_dims[pdf_path] = (pdf.pages[0].width, pdf.pages[0].height)

            for page_idx, page in enumerate(pdf.pages):
                text = page.extract_text() or ""
                found_articles = ART_PATTERN.findall(text)

                for art_no in set(found_articles):
                    if art_no not in index:
                        index[art_no] = []
                    # Avoid duplicate entries for same pdf+page
                    entry = (pdf_path, page_idx, catalog_name)
                    if entry not in index[art_no]:
                        index[art_no].append(entry)

    return index, page_dims


def find_article_positions(pdf_path, page_idx, article_no):
    """
    Find exact positions of an article number on a specific page.
    Returns list of (x0, top, x1, bottom) bounding boxes.
    """
    positions = []
    with pdfplumber.open(pdf_path) as pdf:
        page = pdf.pages[page_idx]
        words = page.extract_words()

        for w in words:
            text = w["text"].strip()
            # Exact match or starts-with match (e.g., "34-7286" in "34-7286.GB")
            if text == article_no:
                positions.append((
                    float(w["x0"]),
                    float(w["top"]),
                    float(w["x1"]),
                    float(w["bottom"])
                ))

    return positions


# ============================================================
# HIGHLIGHT OVERLAY CREATOR
# ============================================================
def create_page_overlay(page_width, page_height, positions,
                        new_page_num=None, source_catalog=None,
                        original_page_num=None):
    """
    Create a transparent PDF overlay with:
    - Highlight rectangles on article positions
    - New page number (top corner, covers original)
    - Source reference footer (catalog name + original page)

    Args:
        page_width, page_height: page dimensions in points
        positions: list of (x0, top, x1, bottom) from pdfplumber coords
        new_page_num: new page number for the combined document
        source_catalog: name of the source catalog file
        original_page_num: original page number in the source catalog

    Returns:
        PdfReader page with overlay content
    """
    buf = BytesIO()
    c = rl_canvas.Canvas(buf, pagesize=(page_width, page_height))

    # ---- Highlight article positions ----
    for (x0, top, x1, bottom) in positions:
        # pdfplumber uses top-down coords, PDF uses bottom-up
        pdf_y_bottom = page_height - bottom
        pdf_y_top = page_height - top
        rect_height = pdf_y_top - pdf_y_bottom

        padding_x = 3
        padding_y = 2

        # Semi-transparent highlight fill
        c.setFillColor(HIGHLIGHT_COLOR)
        c.setStrokeColor(HIGHLIGHT_BORDER)
        c.setLineWidth(1.5)
        c.roundRect(
            x0 - padding_x,
            pdf_y_bottom - padding_y,
            (x1 - x0) + 2 * padding_x,
            rect_height + 2 * padding_y,
            2,
            fill=1,
            stroke=1
        )

        # Small red arrow/marker to the left
        arrow_x = x0 - padding_x - 10
        arrow_y = pdf_y_bottom + rect_height / 2
        c.setFillColor(HIGHLIGHT_BORDER)
        c.setStrokeColor(HIGHLIGHT_BORDER)
        path = c.beginPath()
        path.moveTo(arrow_x, arrow_y + 4)
        path.lineTo(arrow_x + 6, arrow_y)
        path.lineTo(arrow_x, arrow_y - 4)
        path.close()
        c.drawPath(path, fill=1, stroke=0)

    # ---- New page number (top corners, covering originals) ----
    if new_page_num is not None:
        # White rectangles to fully cover original page numbers
        # Top-left area (generous size to cover any original number)
        c.setFillColor(white)
        c.rect(0, page_height - 22, 55, 22, fill=1, stroke=0)
        # Top-right area
        c.rect(page_width - 55, page_height - 22, 55, 22, fill=1, stroke=0)

        # Also cover second-row area (some pages have number indented)
        c.rect(20, page_height - 36, 40, 16, fill=1, stroke=0)
        c.rect(page_width - 60, page_height - 36, 60, 16, fill=1, stroke=0)

        # Draw new page number (top-left, same style as originals)
        c.setFillColor(GEISTER_GRAY)
        c.setFont("Helvetica", 8)
        c.drawString(12, page_height - 13, str(new_page_num))

    # ---- Source reference footer ----
    if source_catalog and original_page_num is not None:
        # Clean up catalog name for display
        display_name = source_catalog.replace("_", " ")
        # Remove file extension if present
        if display_name.lower().endswith(".pdf"):
            display_name = display_name[:-4]

        ref_text = f"Source: {display_name}  |  Original p. {original_page_num}"

        # Small white background strip at very bottom
        c.setFillColor(Color(1, 1, 1, alpha=0.85))
        c.rect(0, 0, page_width, 14, fill=1, stroke=0)

        # Red thin line above
        c.setStrokeColor(GEISTER_RED)
        c.setLineWidth(0.4)
        c.line(10, 14, page_width - 10, 14)

        # Reference text
        c.setFillColor(GEISTER_GRAY)
        c.setFont("Helvetica", 5.5)
        c.drawString(12, 4, ref_text)

        # New page number also on bottom-right for clarity
        if new_page_num is not None:
            c.drawRightString(page_width - 12, 4, f"Page {new_page_num}")

    c.save()
    buf.seek(0)
    overlay_reader = PdfReader(buf)
    return overlay_reader.pages[0]


